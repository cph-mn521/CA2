package DTO;

/**
 *
 * @author Niels Bang
 */
public class ZipCodeErrorDTO {

    String type, title, msg;
    int error;

    public ZipCodeErrorDTO(String type, String title) {
        this.type = type;
        this.title = title;
    }

    public int getError() {
        return error;
    }

    public void setError(int error) {
        this.error = error;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
